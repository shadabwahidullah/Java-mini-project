public class Book {
  String title;
  String author;
  String publisher;
  int position;
  double boughtPrice;

  public Book (String title, String author, String publisher, int position, double boughtPrice) {
    this.title = title;
    this.author = author;
    this.publisher = publisher;
    this.position = position;
    this.boughtPrice = boughtPrice;
  }

}
